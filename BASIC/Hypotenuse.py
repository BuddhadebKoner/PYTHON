# find Hypotenuse of a triangle --


import math
# input adjacent and opposit
a = float(input('Enter the adjacent'))
b = float(input('Enter the opposit'))



# Calculate the hypotenuse
hypotenuse = math.sqrt(a**2 + b**2)

# Display the hypotenuse
print("Hypotenuse of the right-angled triangle: ", hypotenuse)
